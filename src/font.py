# Character width (5) + 1 pixel spacing between chars
CHAR_WIDTH = 5
CHAR_SPACING = 1


def render_text(text: str) -> list[int]:
    """Render a string into a list of 32-bit row values for the LED matrix.

    Returns a list of 16 integers, each representing one row of the display.
    The 7-row font is vertically centered in the 16-row display.
    Bit (total_width - 1) = leftmost pixel (MSB side).

    The bitmap format matches the device protocol: each row is a 32-bit
    big-endian value where higher bits correspond to leftmost pixels.
    """
    text = text.upper()

    # Filter to renderable characters
    chars = []
    for ch in text:
        if ch in FONT_5X7:
            chars.append(ch)
        else:
            chars.append(' ')

    if not chars:
        return [0] * 16

    # Calculate total pixel width
    total_width = len(chars) * CHAR_WIDTH + (len(chars) - 1) * CHAR_SPACING

    # Build 7 font rows by placing each character left to right.
    # Character 0 occupies the highest bit positions (leftmost on display).
    font_rows = [0] * 7
    x_pos = 0  # current x position (0 = leftmost)

    for ch in chars:
        glyph = FONT_5X7[ch]
        for row_idx in range(7):
            # The glyph row is 5 bits: bit 4 = leftmost pixel, bit 0 = rightmost
            # We need to place it so that glyph bit 4 maps to bit (total_width - 1 - x_pos)
            # and glyph bit 0 maps to bit (total_width - 1 - x_pos - 4)
            shift = total_width - x_pos - CHAR_WIDTH
            if shift >= 0:
                font_rows[row_idx] |= glyph[row_idx] << shift
            else:
                font_rows[row_idx] |= glyph[row_idx] >> (-shift)
        x_pos += CHAR_WIDTH + CHAR_SPACING

    # Center the 7 font rows vertically in 16 display rows
    # Place starting at row 4 (4 blank rows above, 5 below)
    rows_16 = [0] * 16
    v_offset = 4
    for i in range(7):
        rows_16[v_offset + i] = font_rows[i]

    return rows_16


def text_width(text: str) -> int:
    """Return the pixel width of rendered text."""
    text = text.upper()
    chars = [ch for ch in text if ch in FONT_5X7]
    if not chars:
        return 0
    return len(chars) * CHAR_WIDTH + (len(chars) - 1) * CHAR_SPACING
